import java.util.*;

public class Snippet {

	/* Instance Variables */
	private String feeling;
	private String sentence;
	
	/* Constructor */
	public Snippet(String feeling, String sentence) {
		this.feeling = feeling;
		this.sentence = sentence;
	}

	/* Returns "feeling" associated with the Snippet */
	public String getFeeling() {
		return feeling;
	}
	
	/* Returns "sentence" associated with the Snippet */
	public String getSentence() {
		return sentence;
	}
}
 
	
